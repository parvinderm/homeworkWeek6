package homework6;
/*Write a Java program to convert a given string into lowercase. Sample
Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.
Output: the quick brown fox jumps over the lazy dog.*/

public class Convert_To_Lowercase {   // Declare Class
    public static void main(String[] args) { // Main Method or Predefined Method

        String name = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG";

        System.out.println("Uppercase :" +name.toLowerCase());

    }
}
